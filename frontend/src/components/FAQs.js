const FAQs = () => {
  const [openIndex, setOpenIndex] = useState(null);

  const toggleQuestion = (index) => {
    setOpenIndex(openIndex === index ? null : index);
  };

  let counter = 0;

  return (
    <div className="faq-container">
      {faqsData.map((section, sectionIdx) => (
        <div key={sectionIdx} className="faq-section">
          <h2 className="faq-category">{section.category}</h2>
          {section.items.map((faq, idx) => {
            const currentIndex = counter++;
            return (
              <div key={idx} className={`faq-item ${openIndex === currentIndex ? "open" : ""}`}>
                <button className="faq-question" onClick={() => toggleQuestion(currentIndex)}>
                  <span>{faq.question}</span>
                  <span>{openIndex === currentIndex ? "˄" : "˅"}</span>
                </button>
                {openIndex === currentIndex && (
                  <div className="faq-answer">
                    {faq.answer.split("\n").map((line, i) => (
                      <p key={i}>{line}</p>
                    ))}
                  </div>
                )}
              </div>
            );
          })}
        </div>
      ))}
    </div>
  );
};

export default FAQs;
